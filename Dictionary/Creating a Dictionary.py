# Creating a Dictionary with integer keys
Dict = {1: 'one', 2: 'two', 3: 'Three', 4: 'Four'}
print("\nCreating a Dictionary with interger keys:")
print(Dict)

# Creating a Dictionary with mixed items

Dict1 = {1: [0,1,2,3], 'name': "imrani", 2.0: "robot"}
print("\nDictionary with mixed items:")
print(Dict1)

# Creating a empty Dictionary
dict = {}
print("\nCreating a empty Dictionary:")
print(dict)

# Creating a Dictionary with dict() method
dict1 = dict({1: 'one', 2: 'two', 3: 'three'})
print("\ncreating a dictionary by using dict() method:")
print(dict1)

# Creating a Dictionary with item as a pair
dict2 = dict([(1,'grapes'),(2,'apple')])
print("\ncreating a Dictionary with item as a pair:")
print(dict2)

# Creating a nested dictionary
dict3 = {1: 'oneRupee', 2: 'TwoRupee', 3: {12: 'oneTwo', 13: 'OneThree', 14: 'OneFour'}}
print("\nCreating a Nested Dictionary")
print(dict3)